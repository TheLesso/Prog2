package paket;

public class Rep_5 {

	public static void main(String[] args) {
		// Loop
		int count1 = 0;
		int count2 = 0;
		while (count1<=5) {
			System.out.println(count1);
			count1++;
		}
		
		do {
			System.out.println(count2);
			count2++;
		} while (count2<=5);
		
		for(int i = 0; i<6; i++) {
			System.out.println(i);
		}
	}

}
